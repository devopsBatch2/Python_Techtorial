
# We use two methods to remove elements from the set
# discard() method and remove ()method.

# remove(valueOfTHeObject)

set = {3,5,7,9}

# I want to remove the number 7 from the set

set.remove(7)

# number 7 will be gone from the set
print(set)

# set.remove(7)

set.discard(3)
print(set)

set.discard(3)

set.add(15)
print(set)
